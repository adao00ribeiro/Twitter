    public void preencherTabela(String sql) throws SQLException{
    ArrayList dados = new ArrayList();
    String [] colunas = new String  [] { "Nome","STATUS","hashtag"};
     conecta.conexao();
     conecta.execultaSql(sql);
     try{
     
     conecta.rs.first();
     do{
     dados.add(new Object [] {conecta.rs.getString("ScreenName"),conecta.rs.getString("status"),conecta.rs.getString("hashtag")});    
     }while(conecta.rs.next());
     }catch(SQLException ex){
     
     }
     ModeloTabela modelo = new ModeloTabela(dados,colunas);
     jtabjogador.setModel(modelo);
     jtabjogador.getColumnModel().getColumn(0).setPreferredWidth(100);
     jtabjogador.getColumnModel().getColumn(0).setResizable(false);
     jtabjogador.getColumnModel().getColumn(1).setPreferredWidth(600);
     jtabjogador.getColumnModel().getColumn(1).setResizable(false);
     jtabjogador.getColumnModel().getColumn(2).setPreferredWidth(100);
     jtabjogador.getColumnModel().getColumn(2).setResizable(false);
     
     
     jtabjogador.getTableHeader().setReorderingAllowed(false);
     jtabjogador.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
     jtabjogador.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
     
    }
